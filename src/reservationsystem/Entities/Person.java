public class Person {
    
    //  <!-------------------------------------------- ATTRIBUTES ------------------------------------------------!>

    private String name;
    
    //  <!--------------------------------------------------------------------------------------------------------->

    //  <!-------------------------------------------- BUILDERS --------------------------------------------------!>
    public Person() {
    }

    public Person(String name) {
        this.name = name;
    }

    //  <!--------------------------------------------------------------------------------------------------------->
    
    //  <!-------------------------------------------- GETTERS AND SETTERS  --------------------------------------!>
    
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


    //  <!--------------------------------------------------------------------------------------------------------->
}
